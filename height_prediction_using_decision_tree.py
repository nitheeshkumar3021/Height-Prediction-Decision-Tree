import pandas as pd
import numpy as np
import matplotlib.pyplot as plt

"""load dataset

"""

dataset=pd.read_csv('/content/dataset.csv')

dataset.shape
dataset.head(5)

x=dataset.iloc[:,:-1].values
y=dataset.iloc[:,-1].values

"""segregate Dataset into input X and output Y"""

from sklearn.model_selection import train_test_split
x_train,x_test,y_train,y_test=train_test_split(x,y,test_size=0.20,random_state=0)

"""traning datset using decision tree"""

from sklearn.tree import DecisionTreeRegressor
model=DecisionTreeRegressor()
model.fit(x_train,y_train)

x_val=np.arange(min(x_test),max(x_test),0.01)
x_val=x_val.reshape((len(x_val),1))
plt.scatter(x_train,y_train,color='red')
plt.plot(x_val,model.predict(x_val),color='green')
plt.title('height prediction using decision tree')
plt.xlabel('age')
plt.ylabel('height')
plt.show()

y_pred=model.predict(x_test)

from sklearn.metrics import r2_score,mean_squared_error
mse=mean_squared_error(y_test,y_pred)
rmse=np.sqrt(mse)
print("Root Mean Square Error:",rmse)
r2score=r2_score(y_test,y_pred)
print("r2score",r2score*100)

